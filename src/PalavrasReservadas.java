public class PalavrasReservadas {
    /* 
        pacotes:
        import = importar pacotes para o codigo
        package = define a que pacote o arquivo pertece
    */
    
    /*
        modificadores de acesso:
        public = pode ser acessado de qualquer class
        private = so pode ser acessado pela propria class
        protected = acesso por class no mesmo pacote ou subclass
    */
    
    /* 
        Variaveis primitivas:
        boolean = boleana
        char = texto de unico caracter
        byte = inteiro de 8 bits
        short = inteiro de 16 bits
        int = inteiro de 32 bits
        long = inteiro de 64 bits
        float = numero com casa decimal de 32 bits
        double = numero com casa decimal de 64 bits
        void = metodo sem retorno ou ausente de valor
    
    */
        
    /*
        modificadores de classes, variaveis ou metodos:
        abstract = class que não pode ser instanciada ou metodo que precisa ser
                    implementado por uma subclass não abstrata
        class = define uma class
        extends = indica a superclasse que a subclasse esta estendendo
        final = impossibilita que uma class seja estendida, que um metodo seja 
                sobrescrito ou que uma variavel seja reinicializada
        implements = indica as interfaces que uma class ira implementar
        interface = especifica uma interface
        native = indica que um metodo esta escrito em uma linguagem diferente
        new = instancia um novo objeto, chamando seu construtor
        static = faz um metodo ou variável pertencer a class ao inves de as instancia
        strictfp = usado para deifinir que um numero com casa decimal, seguiras as regras em metodos e class
        synchronized = indica que um metodo só pode ser acessado por uma thread de cada vez
        transient = impede a uma variavel seja salva em disco   
        volatile = indica que uma variavel pode ser alterada durante uma threads
    */
    
    /*
        controle de fluxo dentro de um bloco {}:
        break = sai do bloco de codigo em que ele esta
        case = executa um bloc de codigo dependedo do teste do switch
        continue - pila a execução do código que viria após essa linha e vai para a próxima passagem do loop
        default: executa esse bloco de código caso nenhum dos teste de switch-case seja verdadeiro;
        do: executa um bloco de código uma vez, e então realiza um teste em conjunto com o while para determinar se o bloco deverá ser executado novamente;
        else: executa um bloco de código alternativo caso o teste "if" seja falso;
        for: usado para realizar um loop condicional de um bloco de código;
        if: usado para realizar um teste lógico de verdadeiro ou falso;
        instanceof: determina se um objeto é uma instância de determinada classe, superclasse ou interface;
        return: retorna um método sem executar qualquer código, que venha depois desta linha (também pode retornar uma variável);
        switch: indica a variável a ser comparada nas expressões case;
        while: executa um bloco de código repetidamente enquanto a condição for verdadeira.
    */
    
}
